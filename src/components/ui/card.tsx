import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "glass" | "neon" | "elevated";
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variants = {
    default: "bg-card border-border/50",
    glass: "bg-card/30 backdrop-blur-xl border-border/30",
    neon: "bg-card border-primary/30 shadow-neon",
    elevated: "bg-gradient-to-br from-card to-secondary border-border/50 shadow-card",
  };

  return (
    <div
      ref={ref}
      className={cn(
        "rounded-xl border text-card-foreground transition-all duration-300",
        variants[variant],
        className
      )}
      {...props}
    />
  );
});
Card.displayName = "Card";

